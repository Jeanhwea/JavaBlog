package net.blog.bean;

public class Timeline {

  String id;
  String user_name;
  String tline_time;
  String tline_action;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getUser_name() {
    return user_name;
  }

  public void setUser_name(String user_name) {
    this.user_name = user_name;
  }

  public String getTline_time() {
    return tline_time;
  }

  public void setTline_time(String tline_time) {
    this.tline_time = tline_time;
  }

  public String getTline_action() {
    return tline_action;
  }

  public void setTline_action(String tline_action) {
    this.tline_action = tline_action;
  }
}
